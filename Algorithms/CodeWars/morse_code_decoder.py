def decode_morse(morse_code: str)->str:
    # Remember - you can use the preloaded MORSE_CODE dictionary:
    # For example: 
    # MORSE_CODE['.-'] = 'A'
    # MORSE_CODE['--...'] = '7'
    # MORSE_CODE['...-..-'] = '$'
    words = morse_code.split("   ")
    res = ""
    for word in words:
        chrs = word.split(" ")
        for chr in chrs:
            if chr:
                letter = MORSE_CODE.get(chr)
                res+=letter
        res+=" "
    return res.strip()

